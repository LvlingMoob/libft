#include "libft.h"

int	ft_atoi(const char *str)
{
	long int	nbr;
	short		neg;

	nbr = 0;
	neg = 1;
	while ((*str >= 9 && *str <= 13) || *str == 32)
		str++;
	if (*str == '-' || *str == '+')
	{
		if (*str == '-')
			neg = -1;
		str++;
	}
	while (*str >= '0' && *str <= '9')
	{
		nbr *= 10;
		nbr += (int)(*str - '0');
		str++;
	}
	nbr *= neg;
	return (nbr);
}
